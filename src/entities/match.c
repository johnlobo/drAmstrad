#include <cpctelera.h>
#include "..//defines.h"
#include "match.h"

// ********************************************************************************
// initMatch
// Returns:
//		void
// ********************************************************************************
void initMatch(TMatch *m)
{
	setMatch(m, 255, 255, 0, 0, 0);
}

// ********************************************************************************
// setMatch
// Returns:
//		void
// ********************************************************************************
void setMatch(TMatch *m, u8 x, u8 y, u8 dir, u8 c, u8 v)
{
	m->x = x;
	m->y = y;
	m->direction = dir;
	m->count = c;
	m->virus = v;
}